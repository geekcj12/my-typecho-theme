// 自动给链接加上blank标签
function themeInit($archive)
{
    if ($archive->is('single'))
    {
        $archive->content = image_class_replace($archive->content);
    }
}
function image_class_replace($content)
{ 
  $content = preg_replace('#<a(.*?) href="([^"]*/)?(([^"/]*)\.[^"]*)"(.*?)>#',
        '<a$1 href="$2$3"$5 target="_blank">', $content);
    return $content;
}
